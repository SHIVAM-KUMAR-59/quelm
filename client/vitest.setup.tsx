const originalError = console.error.bind(console);

beforeAll(() => {
  console.error = (...args: unknown[]) => {
    const msg = String(args[0]);

    if (
      msg.includes("Warning:") ||
      msg.includes("ReactDOM.render") ||
      msg.includes("act(")
    ) {
      return;
    }

    originalError(...args);
  };
});

afterAll(() => {
  console.error = originalError;
});
